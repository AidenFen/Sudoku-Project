void printArray(int sudokuArray[9][9]) {
    string top_bar = "  -----   -----   -----  ";

    // print 
    cout << top_bar << endl;
    for (int x = 0; x < 9; x++) {
        if (x != 0 && x % 3 == 0) { cout << top_bar << endl; }
        for (int y = 0; y < 9; y++) {
            if (y % 3 == 0) { cout << "| "; }
            cout << sudokuArray[x][y] << " ";
        }
        cout << "|" << endl;
    }
    cout << top_bar << endl;
}

